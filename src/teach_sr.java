    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ch= (String)jComboBox1.getSelectedItem();
        String up_marks = this.up_marks.getText();
        String user_in = this.user_in.getText();
        Connection conn;
        PreparedStatement ps;
        try
        {
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/school?user=root&password=root");
            if(ch.equals("Maths"))
            {
                ps=conn.prepareStatement("update student set maths= ? where username = ?");
                ps.setString(1,up_marks);
                ps.setString(2,user_in);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this,"Marks Updated");
            }
            else if(ch.equals("Science"))
            {
                ps=conn.prepareStatement("update student set science= ? where username = ?");
                ps.setString(1,up_marks);
                ps.setString(2,user_in);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this,"Marks Updated");
            }
            else
            {
                ps=conn.prepareStatement("update student set english= ? where username = ?");
                ps.setString(1,up_marks);
                ps.setString(2,user_in);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this,"Marks Updated");
            }
            
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

