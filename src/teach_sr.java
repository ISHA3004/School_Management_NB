    public teach_sr(String username) {
        initComponents();
        model=new DefaultTableModel();
        model.addColumn("Username");
        model.addColumn("Name");
        model.addColumn("maths");
        model.addColumn("science");
        model.addColumn("english");
        model.addColumn("total_marks");
        model.addColumn("percentage");
        
        this.username =username;
        
        try
        {
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/school?user=root&password=root");
            ps=conn.prepareStatement("select * from stud_result");
            ResultSet rs = ps.executeQuery();
            
            while(rs.next())
            {
                model.addRow(new Object[] {rs.getInt("username"),rs.getString("name"),rs.getInt("maths"),rs.getInt("science"),rs.getInt("english"),rs.getInt("total_marks"),rs.getFloat("percentage")});
            }
            
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(this, ex);
        }
        sr_table.setModel(model);
    
        
    }

