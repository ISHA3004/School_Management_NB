    public teach_sa(String username) {
        initComponents();
        this.username= username;
        model=new DefaultTableModel();
        model.addColumn("Username");
        model.addColumn("Name");
        model.addColumn("Lec attended");
        model.addColumn("Attendance percent");
        
        try
        {
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/school?user=root&password=root");
            ps=conn.prepareStatement("select * from stud_attendance");
            ResultSet rs = ps.executeQuery();
            
            while(rs.next())
            {
                model.addRow(new Object[] {rs.getInt("username"),rs.getString("name"),rs.getInt("lec_attend"),rs.getFloat("attend_per")});
            }
            
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(this, ex);
        }
        sa_table.setModel(model);
    }

