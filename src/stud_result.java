    public stud_result(String username) {
        initComponents();
        this.username= username;
        Connection conn;
        PreparedStatement ps;
        try
        {
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/school?user=root&password=root");
            ps=conn.prepareStatement("select * from stud_result where username = ?");
            ps.setString(1,username);
            ResultSet rs=ps.executeQuery();
            while(rs.next())
            {
                jLabel2.setText("Science : "+rs.getString("science"));
                jLabel3.setText("Maths : "+rs.getString("maths"));
                jLabel4.setText("English : "+rs.getString("english"));
                jLabel6.setText("Total marks "+rs.getString("total_marks"));
                jLabel7.setText("Percentage : "+rs.getString("percentage"));
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(this,ex);
        }
        
        
    }

